// 1 - O que é uma classe e qual a sua composição?
// É um tipo estruturado com membros (Atributos e Métodos). Representa uma abstração de uma entidade do mundo real.

// 2 - O que é um objeto e como pode ser criado?
// Uma instância de uma classe alocada na memória Heap. Um objeto pode ser criado com o uso da palavra reservada 'new'.

// 3 - Qual a diferença entre classe e objeto?
// Classe é um tipo estruturado e o objeto é a materialização da classe.

// 4 - O que é um método e como pode ser criado e chamado?
// Um método é um bloco de código que especifica um comportamento de uma classe.

// 5 - O que é um  parâmetro de método?
// Um valor que um método espera receber quando for chamado para utilizá-lo em sua rotina.

// 6 - O que é um argumento?
// Argumento é o valor do parâmetro do método informado na sua chamada.

// 7 - O que é um construtor e qual o seu objetivo?
// É um método com o mesmo nome da classe utilizado para inicialização de sua instância.

// 8 - Quais os usos da palavra chave this?
//Refere-se à instância atual da classe e pode ser usada também para passar um objeto da instância atual
//para outros métodos.

// 9 - O que é um método com retorno? 
// Um método que retorna algum valor para a classe que o chamou através da palavra reservada 'return'.

// 10 - O que é sobrecarga de método?
// Quando há métodos com o mesmo nome na mesma classe, mas com assinaturas diferentes.

// 11 - Como  é feita  a passagem de parâmetros por valor?
// O método recebe uma cópia do valor passado.

// 12 - Como  é feita  a passagem de parâmetros por referência?
// O método recebe uma cópia da referência passada.

// 13 - Qual a diferença entre os parâmetros out e o parâmetro ref?
// Parâmetro Ref precisa ser inicializado.
// Parâmetro Out não precisa ser inicializado.

// 14 - O que são argumentos nomeados?
// Permite alterar a ordem dos parâmetros na chamada nomeando-os.

// 15 - O que são parâmetros opcionais? 
// Define-se um valor default para o parâmetro tonando a passagem de argumento deste parâmetro opcional.

// 16 - Qual o efeito da palavra-chave static?
// O uso de static torna o membro da classe relativo à classe e não à um objeto da mesma.

// 17 - O que é método estático e qual o seu comportamento?
// Métodos estáticos são aqueles que não dependem de uma instância da classe para serem chamados.

// 18 - O que é um campo estático e qual o seu comportamento?
// São os campos que pertencem à classe e não à uma instância da mesma.
// São valores compartilhados por todas as instâncias daquela classe.

// 19 - O que são propriedades?
// São os campos de uma classe encapsulados com acessadores.

// 20 - Qual a diferença entre campos e propriedades?
// As propriedades expõem os campos, que por sua vez, ficam privados.